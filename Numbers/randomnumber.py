#random numers
import random
print(random.random()) # creates a random between 0.0 and 1.0

print(random.randint(1, 10)) # creates a random integer between 1 and 10 (inclusive)

print(random.choice([1, 2, 3, 4, 5])) # creates a random choice from a list
print(random.choice("Hello World")) # creates a random choice from a string
