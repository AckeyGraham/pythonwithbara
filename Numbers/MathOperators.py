# Math operators for numbers
print(1 + 2)  # Addition
print(5 - 3)  # Subtraction
print(4 * 6)  # Multiplication 
print(10 / 2)  # Division
print(7 % 3)  # Modulus (remainder) ( used in odd or even number checking)
print(2 ** 3)  # Exponentiation (2 raised to the power of 3) 2*2*2
print(10 // 3)  # Floor division (quotient without the remainder)
# Output:
# 3
# 2
# 24
# 5.0
# 1
# 8
# 3

x = 2
x = x + 3  # Increment x by 3
print(x)  # Output: 5

#alternatively, you can use the shorthand operator:
x = 2
x += 3  # This is equivalent to x = x + 3
print(x)  # Output: 5 

x = x *2
print(x)  # Output: 10
x *= 2
print(x)  # Output: 20